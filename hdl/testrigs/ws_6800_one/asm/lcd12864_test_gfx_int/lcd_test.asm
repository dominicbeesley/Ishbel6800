		asla
		asla
		asla
		adda	#<BCDFONT		
		staa	zp_ch_ptr+1
		ldaa	#>BCDFONT
		adca	#0
		staa	zp_ch_ptr

		; zp_ch_ptr now points at font, transfer to zp_row_ptr
		ldb	#8
.1:		ldx	zp_ch_ptr
		ldaa	0,X
		inx
		stx	zp_ch_ptr
		ldx	zp_row_ptr
		staa	0,X
		inx
		stx	zp_row_ptr
		decb
		bne	.1
		rts


	; need ~ 2500 cycles
delay1ms:	psha		;4
		pshb
		ldb	#6
		clra		;2
.0:		deca		;512
		nop		;512
		bne	.0	;1024
		decb 
		bne	.0
		pulb
		pula		;4
		rts		;4
delayXms:	jsr	delay1ms
		dex
		bne	delayXms
		rts


lcd_wait:	psha
.1:		lda	LCD_12864_REG
		bmi	.1
		pula
		rts

lcd_reg:		jsr	lcd_wait
		staa	LCD_12864_REG
		rts

lcd_data:	jsr	lcd_wait
		staa	LCD_12864_DATA
		rts



handle_irq:
		ldaa	VIA_ifr
		bpl	@notirq
		anda	#VIA_IFR_BIT_T1
		beq	@notirq
		staa	VIA_ifr	; clear flag

		dec	zp_ctr100
		bne	@sk1

		ldaa	#100
		staa	zp_ctr100


		ldab	#CLOCKSZ
		ldx	#zp_seconds+CLOCKSZ-1
		sec
@incloop:	ldaa	0,X
		adca	#0
		daa
		staa	0,X
		dex
		decb
		bne	@incloop
@skinc:		

@sk1:		rti

@notirq:	ldx	old_IRQV
		jmp	0,X			; pass on interrupt

		.data		

